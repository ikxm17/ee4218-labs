// ! Can refer to app_main.elf.size for size of memory segments
#define AXIDMA_BUFFER_BASE_ADDR_OFFSET \
    0x01000000
#define AXIDMA_BUFFER_BASE_ADDR         DDR_BASE_ADDR + AXIDMA_BUFFER_BASE_ADDR_OFFSET
